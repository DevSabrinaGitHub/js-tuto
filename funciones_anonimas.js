'use strict'

/*funcion anonima: es una funcion que no tiene un nombre, es decir, el  conjunto de 
intrucciones no tendra ningun nombre pero se podra guardar en una variable*/

var pelicula= function(nombre){
    return "la pelicula es: "+nombre;   
}

/*escribimos en la consola:

pelicula(" scary movie")

automaticamente saldria debajo: 

la pelicula es scary movie */