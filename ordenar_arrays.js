'use strict'

var frutas = ["manzana", "banana", "cereza"];

console.log(frutas);

/* ordenar un array con el metodo sort  */

frutas.sort();

console.log(frutas);   //imprimira el array en orden alfabetico

/*
(3): [banana, cereza, manzana];
*/


/* darle la vuelta a un array con reverse  */

frutas.reverse();

console.log(frutas);     

/* imprime 
es decir imprime lo que anteriormente estaba con sort , imprime los ultimos elementos para ser primeros
(3): [manzana, cereza, banana]   */